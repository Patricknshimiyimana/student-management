    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        model = (DefaultTableModel) jTable1.getModel();
        rowIndex = jTable1.getSelectedRow();
        jTextField3.setText(model.getValueAt(rowIndex, 0).toString());
        jTextField4.setText(model.getValueAt(rowIndex, 1).toString());

        String gender = model.getValueAt(rowIndex, 3).toString();
        if (gender.equals("Female")) {
            jComboBox1.setSelectedIndex(1);
        } else {
            jComboBox1.setSelectedIndex(0);

        }
        String age = model.getValueAt(rowIndex, 2).toString();
        if (age.equals("18")) {
            jComboBox3.setSelectedIndex(0);
        } else if (age.equals("19")) {
            jComboBox3.setSelectedIndex(1);
        } else if (age.equals("20")) {
            jComboBox3.setSelectedIndex(2);
        } else if (age.equals("21")) {
            jComboBox3.setSelectedIndex(3);
        } else if (age.equals("22")) {
            jComboBox3.setSelectedIndex(4);
        } else if (age.equals("23")) {
            jComboBox3.setSelectedIndex(5);
        } else if (age.equals("24")) {
            jComboBox3.setSelectedIndex(6);
        } else if (age.equals("25")) {
            jComboBox3.setSelectedIndex(7);
        } else {
            jComboBox3.setSelectedIndex(4);
        }

        jTextField5.setText(model.getValueAt(rowIndex, 4).toString());
        jTextField6.setText(model.getValueAt(rowIndex, 5).toString());
        jTextField7.setText(model.getValueAt(rowIndex, 6).toString());
        jTextField8.setText(model.getValueAt(rowIndex, 7).toString());
        jTextField9.setText(model.getValueAt(rowIndex, 8).toString());
        String path = model.getValueAt(rowIndex, 9).toString();
        imagePath = path;
        jLabelImage.setIcon(imageAdjust(path, null)); // get image path and called imageAdjust method convert path to imageIcon

    }//GEN-LAST:event_jTable1MouseClicked

